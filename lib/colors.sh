#!/usr/bin/env bash
# ═══════════════════════════════════════════════════════════════════
# Howe_Linux_sh — 颜色 / 日志工具
# ═══════════════════════════════════════════════════════════════════

R='\033[0;31m'   # 红
G='\033[0;32m'   # 绿
Y='\033[1;33m'   # 黄
B='\033[0;34m'   # 蓝
C='\033[0;36m'   # 青
W='\033[1m'      # 粗体
DIM='\033[2m'    # 暗淡
N='\033[0m'      # 重置

log()   { echo -e "${G}[✓]${N} $*"; }
warn()  { echo -e "${Y}[!]${N} $*"; }
err()   { echo -e "${R}[✗]${N} $*"; return 1; }
info()  { echo -e "${B}[·]${N} $*"; }

# 带标题的分隔线
section() {
  local title="$1"
  echo -e "\n${W}${C}── $title ──────────────────────────────${N}"
}
